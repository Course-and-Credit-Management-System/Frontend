function updateJson(filePath, newValues) {
  let data = {};
  if (fs.existsSync(filePath)) {
    try {
      data = JSON.parse(fs.readFileSync(filePath, 'utf-8'));
    } catch(e) {
      console.error(`Invalid JSON in ${filePath}`);
    }
  }
  let modified = false;
  for (const [enKey, translation] of Object.entries(newValues)) {
    const isEn = filePath.includes('\\en\\') || filePath.includes('/en/');
    const finalVal = isEn ? enKey : translation;
    if (data[enKey] !== finalVal) {
      data[enKey] = finalVal;
      modified = true;
    }
  }
  if (modified) {
    fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf-8');
    console.log(`Updated ${filePath}`);
  }
}

