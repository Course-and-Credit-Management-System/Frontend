// Helper to inject useTranslation safely
function injectI18n(content) {
  if (!content.includes('import { useTranslation }')) {
    content = content.replace(/(import React[^;]*;)/, '$1\nimport { useTranslation } from "react-i18next";');
  }
  if (!content.includes('const { t } = useTranslation();')) {
    // Basic finding of React component start
    const match = content.match(/const\s+\w+\s*:\s*React\.FC<[^>]*>\s*=\s*\([^)]*\)\s*=>\s*\{/);
    if (match) {
      content = content.replace(match[0], `${match[0]}\n  const { t } = useTranslation();`);
    } else {
      const match2 = content.match(/const\s+\w+\s*=\s*\([^)]*\)\s*=>\s*\{/);
      if (match2) {
        content = content.replace(match2[0], `${match2[0]}\n  const { t } = useTranslation();`);
      }
    }
  }
  return content;
}

